A = {1,2,3,4,5}
B = {2,4,6,8,10}

# creating a new set using intersection, taking common items
intersection = A & B
print(intersection)

# Union means taking all common items that are present in both sets
union = A | B
print(union)

# it means it will be present in one set
commonInOne = A ^ B
print(commonInOne)

